"""Stage 6 v2 gate: packages a search_scorer CODE SNAPSHOT (a file path, not a weights overlay
-- for a tie-break/logic change, not a weight change) vs. the local rule-based `baseline` agent
as a Kaggle kernel, runs it at 4-worker parallelism, and (with --wait) polls until it finishes
and pulls the result back down. See docs/tie_break_v2_2026-07-23.md for why.

Run this TWICE with the same --seed, once per code snapshot (v1 frozen, v2 working tree), to
get a paired-by-seed win rate comparison against the common `baseline` opponent -- that's
"winrate v1 vs v2" read correctly (see the plan/report: there is no direct v1-vs-v2 match).

PACKAGING (confirmed 2026-07-23, the hard way): `kaggle kernels push -p <dir>` for a
kernel_type "script" does NOT upload sibling files alongside the entry `code_file` -- a real
kernel run's downloaded log showed `/kaggle/src` containing only `['__script__.ipynb',
'script.py']`, i.e. every other file in the pushed directory (sdk_path.py, obs.py, baseline.py,
determinize.py, search_scorer.py, deck.csv) was silently dropped. `scripts/build_kernel_
bakeoff.py` has this exact same latent bug (copies sibling files the same way) -- it was never
actually exercised end-to-end before this discovery. The fix here: package everything as ONE
self-contained driver.py. Each required module's source is read from disk, base64-encoded (so
no quoting/escaping of the source's own quotes/braces/backslashes is ever needed), embedded as
a literal, and installed into sys.modules via exec() at driver startup, in dependency order,
before any `from sdk_path import ...`-style cross-import happens. deck.csv's 60 card ids are
embedded as a literal Python list for the same reason.

CHECKPOINTING (best-effort, confirmed via AskUserQuestion): Kaggle kernels are stateless
between separate `kernel push` runs, so true mid-run resume isn't literally possible. Instead:
(1) the driver writes its result file every ~20 games, not just at the end, so a timed-out/
killed kernel still leaves usable partial data in kernel output; (2) if `--wait` detects the
fetched result is partial (games_completed < the games requested this run) or the kernel
errored, this script automatically builds and pushes a FOLLOW-UP kernel for just the remaining
games (continuing the same seed sequence) and merges the two partial results locally into one
combined JSON -- so a single `--push --wait` invocation transparently recovers from one
interruption without discarding completed games.

Output-directory and SDK-mount-path handling (also confirmed the hard way on real kernel runs):
`/kaggle/working` is where `kaggle kernels output` looks, NOT `/kaggle/src` (where the script
itself runs from) -- output files are written there explicitly. The competition-attached SDK
mount is at `/kaggle/input/competitions/pokemon-tcg-ai-battle/...` (an extra "competitions/"
segment the very first guess omitted).

Usage:
  .venv/Scripts/python scripts/build_kernel_vs_baseline.py \\
      --code runs/v2_tie_break/search_scorer_v1_snapshot.py --label v1 \\
      --games 400 --seed 0 --workers 4 --slug tie-break-v1-vs-baseline-2026-07-23 \\
      --push --wait
"""


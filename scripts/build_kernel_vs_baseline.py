KERNEL_METADATA_TEMPLATE = {
    "id": "",
    "title": "",
    "code_file": "driver.py",
    "language": "python",
    "kernel_type": "script",
    "is_private": True,
    "enable_gpu": False,
    "enable_internet": False,
    "competition_sources": [SIMULATION_COMPETITION],
    "dataset_sources": [],
    "kernel_sources": [],
}


def _read_module_source(name: str, search_scorer_path: str) -> str:
    if name == "search_scorer":
        path = search_scorer_path
    else:
        path = os.path.join(SRC_DIR, f"{name}.py")
    with open(path, encoding="utf-8") as f:
        return f.read()


def _read_deck(path: str) -> list[int]:
    with open(path, encoding="utf-8") as f:
        return [int(x) for x in f.read().split("\n")[:60] if x.strip()]


def build(code_path: str, games: int, start_game_index: int, seed_base: int, workers: int,
          slug: str) -> str:
    kernel_dir = os.path.join(KERNEL_ROOT, slug)
    if os.path.isdir(kernel_dir):
        # Windows occasionally holds a transient handle on a just-used directory (antivirus/
        # indexer scan, or a shell whose cwd briefly pointed inside it) -- confirmed 2026-07-23
        # this is transient, not a real conflict, so retry briefly instead of failing outright.
        for attempt in range(5):
            try:
                shutil.rmtree(kernel_dir)
                break
            except PermissionError:
                if attempt == 4:
                    raise
                time.sleep(2)
    os.makedirs(kernel_dir)

    module_sources_b64 = {
        name: base64.b64encode(_read_module_source(name, code_path).encode("utf-8")).decode(
            "ascii")
        for name in MODULE_ORDER
    }
    deck = _read_deck(DECK_PATH)

    driver_source = DRIVER_TEMPLATE % {
        "module_sources_b64": module_sources_b64,
        "module_order": MODULE_ORDER,
        "games": games,
        "start_game_index": start_game_index,
        "seed_base": seed_base,
        "workers": workers,
        "deck": deck,
    }
    with open(os.path.join(kernel_dir, "driver.py"), "w", encoding="utf-8") as f:
        f.write(driver_source)

    metadata = dict(KERNEL_METADATA_TEMPLATE)
    metadata["id"] = f"{get_kaggle_username()}/{slug}"
    metadata["title"] = slug.replace("-", " ")
    with open(os.path.join(kernel_dir, "kernel-metadata.json"), "w", encoding="utf-8") as f:
        json.dump(metadata, f, indent=2)

    print(f"Kernel package assembled at {kernel_dir} (id={metadata['id']}, "
          f"driver.py={len(driver_source)} bytes)")
    return kernel_dir


def push(kernel_dir: str) -> None:
    subprocess.run(kaggle_cmd("kernels", "push", "-p", kernel_dir), check=True)


def poll_and_fetch(slug: str, kernel_dir: str, poll_interval_s: float = 30.0,
                    timeout_s: float = 3600.0) -> dict | None:
    kernel_ref = f"{get_kaggle_username()}/{slug}"
    status_cmd = kaggle_cmd("kernels", "status", kernel_ref)
    start = time.time()
    while time.time() - start < timeout_s:
        result = subprocess.run(status_cmd, capture_output=True, text=True)
        print(result.stdout.strip())
        if "complete" in result.stdout.lower():
            break
        if "error" in result.stdout.lower() or "failed" in result.stdout.lower():
            print("Kernel run failed -- fetch output anyway for the error log/partial result.",
                  file=sys.stderr)
            break
        time.sleep(poll_interval_s)
    else:
        print(f"Timed out after {timeout_s}s waiting for kernel completion -- fetching "
              f"whatever partial output exists.", file=sys.stderr)

    output_cmd = kaggle_cmd("kernels", "output", kernel_ref, "-p", kernel_dir)
    subprocess.run(output_cmd, check=False)
    result_path = os.path.join(kernel_dir, "vs_baseline_result.json")
    if os.path.exists(result_path):
        with open(result_path, encoding="utf-8") as f:
            return json.load(f)
    print(f"No vs_baseline_result.json found in kernel output at {kernel_dir} -- check the "
          f"kernel's log.", file=sys.stderr)
    return None


def merge_results(parts: list[dict]) -> dict:
    """Combines one or more (possibly partial) run results -- from the checkpointed-resume
    follow-up kernel(s) -- into one final aggregate, recomputing win_rate/CI over the pooled
    games rather than averaging per-run rates."""
    wins = sum(p["wins"] for p in parts)
    losses = sum(p["losses"] for p in parts)
    draws = sum(p["draws"] for p in parts)
    crashes = sum(p["crashes"] for p in parts)
    n_decided = wins + losses + draws
    win_rate = (wins + 0.5 * draws) / n_decided if n_decided else 0.0
    se = math.sqrt(win_rate * (1 - win_rate) / n_decided) if n_decided else 0.0
    ci = (max(0.0, win_rate - 1.96 * se), min(1.0, win_rate + 1.96 * se))
    return {
        "games_requested": sum(p["games_requested"] for p in parts if not p.get("partial")
                                or p is parts[-1]),
        "games_completed": sum(p["games_completed"] for p in parts),
        "partial": False, "wins": wins, "losses": losses, "draws": draws, "crashes": crashes,
        "candidate_win_rate": win_rate, "win_rate_95ci": ci, "n_runs_merged": len(parts),
    }


def run_with_resume(code_path: str, label: str, games: int, seed_base: int, workers: int,
                     base_slug: str, wait_timeout_s: float) -> dict | None:
    """Pushes one kernel for `games` games; if the fetched result is partial (or missing),
    builds+pushes a follow-up kernel for the remainder, continuing the same seed sequence, and
    merges results. Bounded to a few follow-up attempts so a persistently-broken kernel doesn't
    loop forever."""
    parts = []
    start_index = 0
    remaining = games
    attempt = 0
    max_attempts = 4
    while remaining > 0 and attempt < max_attempts:
        attempt += 1
        slug = base_slug if attempt == 1 else f"{base_slug}-resume{attempt}"
        kernel_dir = build(code_path, remaining, start_index, seed_base, workers, slug)
        push(kernel_dir)
        result = poll_and_fetch(slug, kernel_dir, timeout_s=wait_timeout_s)
        if result is None:
            print(f"[{label}] attempt {attempt}: no result at all -- stopping resume attempts.",
                  file=sys.stderr)
            break
        parts.append(result)
        completed = result["games_completed"]
        start_index += completed
        remaining -= completed
        if not result.get("partial") and completed >= result["games_requested"]:
            break
        if completed == 0:
            print(f"[{label}] attempt {attempt}: 0 games completed -- stopping resume attempts "
                  f"to avoid a retry loop against a broken kernel.", file=sys.stderr)
            break
        print(f"[{label}] attempt {attempt}: {completed} games completed, {remaining} "
              f"remaining -- launching a follow-up kernel." if remaining > 0 else
              f"[{label}] attempt {attempt}: all {games} games completed.")

    if not parts:
        return None
    return merge_results(parts) if len(parts) > 1 else parts[0]


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--code", required=True,
                     help="path to the search_scorer.py CODE SNAPSHOT to package (e.g. the "
                          "frozen v1 copy, or the current working-tree v2 file)")
    ap.add_argument("--label", required=True, help="e.g. v1 or v2 -- used in the slug/output")
    ap.add_argument("--games", type=int, default=400)
    ap.add_argument("--seed", type=int, default=0, help="shared SEED_BASE -- use the SAME "
                     "value for both the v1 and v2 runs so they're paired by seed")
    ap.add_argument("--workers", type=int, default=4)
    ap.add_argument("--slug", default=None)
    ap.add_argument("--push", action="store_true")
    ap.add_argument("--wait", action="store_true")
    ap.add_argument("--wait-timeout-s", type=float, default=3600.0)
    ap.add_argument("--out", default=None, help="where to write the final merged result JSON "
                     "locally (default: runs/kernel_vs_baseline/<slug>_final_result.json)")
    args = ap.parse_args()

    slug = args.slug or f"tie-break-{args.label}-vs-baseline-2026-07-23"
    out_path = args.out or os.path.join(KERNEL_ROOT, f"{slug}_final_result.json")

    if not args.push:
        build(args.code, args.games, 0, args.seed, args.workers, slug)
        print("Package built but not pushed (pass --push). Review kernel-metadata.json's "
              "\"id\" field first.")
        return

    if not args.wait:
        kernel_dir = build(args.code, args.games, 0, args.seed, args.workers, slug)
        push(kernel_dir)
        print("Pushed without --wait -- check status manually with "
              f"`kaggle kernels status {get_kaggle_username()}/{slug}`.")
        return

    result = run_with_resume(args.code, args.label, args.games, args.seed, args.workers, slug,
                              args.wait_timeout_s)
    if result is None:
        sys.exit(f"[{args.label}] no usable result after all resume attempts.")
    os.makedirs(os.path.dirname(out_path), exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(result, f, indent=2)
    print(f"\n[{args.label}] FINAL: {json.dumps(result, indent=2)}")
    print(f"wrote {out_path}")


if __name__ == "__main__":
    main()

def push(kernel_dir: str) -> None:
    subprocess.run(kaggle_cmd("kernels", "push", "-p", kernel_dir), check=True)


def poll_and_fetch(slug: str, kernel_dir: str, poll_interval_s: float = 30.0,
                    timeout_s: float = 3600.0) -> dict | None:
    kernel_ref = f"{get_kaggle_username()}/{slug}"
    status_cmd = kaggle_cmd("kernels", "status", kernel_ref)
    start = time.time()
    while time.time() - start < timeout_s:
        result = subprocess.run(status_cmd, capture_output=True, text=True)
        print(result.stdout.strip())
        if "complete" in result.stdout.lower():
            break
        if "error" in result.stdout.lower() or "failed" in result.stdout.lower():
            print("Kernel run failed -- fetch output anyway for the error log/partial result.",
                  file=sys.stderr)
            break
        time.sleep(poll_interval_s)
    else:
        print(f"Timed out after {timeout_s}s waiting for kernel completion -- fetching "
              f"whatever partial output exists.", file=sys.stderr)

    output_cmd = kaggle_cmd("kernels", "output", kernel_ref, "-p", kernel_dir)
    subprocess.run(output_cmd, check=False)
    result_path = os.path.join(kernel_dir, "head_to_head_result.json")
    if os.path.exists(result_path):
        with open(result_path, encoding="utf-8") as f:
            return json.load(f)
    print(f"No head_to_head_result.json found in kernel output at {kernel_dir} -- check the "
          f"kernel's log.", file=sys.stderr)
    return None


def merge_results(parts: list[dict]) -> dict:
    a_wins = sum(p["a_wins"] for p in parts)
    b_wins = sum(p["b_wins"] for p in parts)
    draws = sum(p["draws"] for p in parts)
    crashes = sum(p["crashes"] for p in parts)
    n_decided = a_wins + b_wins + draws
    b_win_rate = (b_wins + 0.5 * draws) / n_decided if n_decided else 0.0
    se = math.sqrt(b_win_rate * (1 - b_win_rate) / n_decided) if n_decided else 0.0
    ci = (max(0.0, b_win_rate - 1.96 * se), min(1.0, b_win_rate + 1.96 * se))
    return {
        "label_a": parts[0]["label_a"], "label_b": parts[0]["label_b"],
        "games_requested": sum(p["games_requested"] for p in parts if not p.get("partial")
                                or p is parts[-1]),
        "games_completed": sum(p["games_completed"] for p in parts),
        "partial": False, "a_wins": a_wins, "b_wins": b_wins, "draws": draws,
        "crashes": crashes, "b_win_rate": b_win_rate, "b_win_rate_95ci": ci,
        "n_runs_merged": len(parts),
    }


def run_with_resume(code_a: str, label_a: str, code_b: str, label_b: str, games: int,
                     seed_base: int, workers: int, base_slug: str,
                     wait_timeout_s: float) -> dict | None:
    parts = []
    start_index = 0
    remaining = games
    attempt = 0
    max_attempts = 4
    while remaining > 0 and attempt < max_attempts:
        attempt += 1
        slug = base_slug if attempt == 1 else f"{base_slug}-resume{attempt}"
        kernel_dir = build(code_a, label_a, code_b, label_b, remaining, start_index, seed_base,
                            workers, slug)
        push(kernel_dir)
        result = poll_and_fetch(slug, kernel_dir, timeout_s=wait_timeout_s)
        if result is None:
            print(f"attempt {attempt}: no result at all -- stopping resume attempts.",
                  file=sys.stderr)
            break
        parts.append(result)
        completed = result["games_completed"]
        start_index += completed
        remaining -= completed
        if not result.get("partial") and completed >= result["games_requested"]:
            break
        if completed == 0:
            print(f"attempt {attempt}: 0 games completed -- stopping resume attempts to avoid "
                  f"a retry loop against a broken kernel.", file=sys.stderr)
            break
        print(f"attempt {attempt}: {completed} games completed, {remaining} remaining -- "
              f"launching a follow-up kernel." if remaining > 0 else
              f"attempt {attempt}: all {games} games completed.")

    if not parts:
        return None
    return merge_results(parts) if len(parts) > 1 else parts[0]


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--code-a", required=True, help="reference snapshot, e.g. frozen v1")
    ap.add_argument("--label-a", required=True)
    ap.add_argument("--code-b", required=True, help="candidate snapshot, e.g. v3 working tree")
    ap.add_argument("--label-b", required=True)
    ap.add_argument("--games", type=int, default=400)
    ap.add_argument("--seed", type=int, default=0)
    ap.add_argument("--workers", type=int, default=4)
    ap.add_argument("--slug", default=None)
    ap.add_argument("--push", action="store_true")
    ap.add_argument("--wait", action="store_true")
    ap.add_argument("--wait-timeout-s", type=float, default=3600.0)
    ap.add_argument("--out", default=None)
    args = ap.parse_args()

    slug = args.slug or f"{args.label_b}-vs-{args.label_a}-head-to-head-2026-07-24"
    out_path = args.out or os.path.join(KERNEL_ROOT, f"{slug}_final_result.json")

    if not args.push:
        build(args.code_a, args.label_a, args.code_b, args.label_b, args.games, 0, args.seed,
              args.workers, slug)
        print("Package built but not pushed (pass --push). Review kernel-metadata.json's "
              "\"id\" field first.")
        return

    if not args.wait:
        kernel_dir = build(args.code_a, args.label_a, args.code_b, args.label_b, args.games, 0,
                            args.seed, args.workers, slug)
        push(kernel_dir)
        print("Pushed without --wait -- check status manually with "
              f"`kaggle kernels status {get_kaggle_username()}/{slug}`.")
        return

    result = run_with_resume(args.code_a, args.label_a, args.code_b, args.label_b, args.games,
                              args.seed, args.workers, slug, args.wait_timeout_s)
    if result is None:
        sys.exit("no usable result after all resume attempts.")
    os.makedirs(os.path.dirname(out_path), exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(result, f, indent=2)
    print(f"\nFINAL: {json.dumps(result, indent=2)}")
    print(f"wrote {out_path}")


if __name__ == "__main__":
    main()

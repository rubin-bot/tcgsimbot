"""Stage 6 v3 gate, leg (a): packages TWO search_scorer CODE SNAPSHOTS (e.g. the frozen v1 --
pre-tie-break, actually-shipped-to-Kaggle -- and the post-A1 v3 working tree) as a single
Kaggle kernel and plays them directly against each other, 4-worker parallelism, --wait polls
and pulls the result back down. Fork of scripts/build_kernel_vs_baseline.py (same proven
self-contained-driver packaging -- see that script's docstring for why sibling-file packaging
doesn't work on Kaggle script kernels, and the /kaggle/working vs /kaggle/src / SDK mount-path
details, all reused verbatim here).

The only structural difference from build_kernel_vs_baseline.py: TWO search_scorer sources are
embedded, each installed into sys.modules under a DISTINCT aliased name ("search_scorer_a" /
"search_scorer_b") instead of one "search_scorer" vs the separately-embedded baseline module.
Internal `from determinize import ...`-style imports inside each snapshot's source still resolve
correctly regardless of the outer module's own registered name, since Python's import system
checks sys.modules by the IMPORTED name, not the importing module's identity -- the shared
modules (sdk_path/obs/baseline/determinize) are installed once, before either snapshot.

Usage:
  .venv/Scripts/python scripts/build_kernel_head_to_head.py \\
      --code-a runs/v2_tie_break/search_scorer_v1_snapshot.py --label-a v1 \\
      --code-b runs/v3_voting/search_scorer_v3_snapshot.py --label-b v3 \\
      --games 400 --seed 0 --workers 4 --slug v3-v1-head-to-head-2026-07-24 \\
      --push --wait
"""

